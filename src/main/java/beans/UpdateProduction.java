package beans;

public class UpdateProduction {
	private String inputStatement;
	private String outputStatement;
	private Integer count = 0;
	
	public String getInputStatement() {
		return inputStatement;
	}

	public void setInputStatement(String inputStatement) {
		this.inputStatement = inputStatement;
	}

	public String getOutputStatement() {
		return outputStatement;
	}

	public void setOutputStatement(String outputStatement) {
		this.outputStatement = outputStatement;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}

	
}
